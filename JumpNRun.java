    public void GenerateLevel()
    {
        Coins = 0;
        Objects = 0;

        int px = TILESIZE/2,
            py = TILESIZE/2;
        
        

        for (int i = 0 ; i < 28; i++)
        {
            
            for (int j = 0 ; j < 28; j++)
            {
                int ID = LVL_data[i][j] ;
                
                if (ID >= 1 && 
                    ID <= 3)
                {

                     addObject( new Block(ID), px, py );
                     Objects++;
                }
                else
                if (ID == 4)
                {
                     addObject( new Box(), px, py );
                     Objects++;
                }
                else
                if (ID == 5)
                {

                     addObject( Player, px, py );
                     Objects++;
                }
                else
                if (ID == 6)
                {
                     addObject( new Coin(), px, py );
                     Coins++;
                     Objects++;
                }
                else
                if (ID == 7 || ID == 8)
                {
                    addObject ( new Enemy(ID), px, py);
                    Objects++;
                }

                px += TILESIZE;
            }
            
            px = TILESIZE/2;
            py += TILESIZE;
        }
        
        maploaded = true;
    }
    
    
