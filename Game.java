  public void GenerateLevel() {
    Coins = 0;
    Objects = 0;

    int px = TILESIZE / 2, py = TILESIZE / 2;

    for (int i = 0; i < 28; i++) {
      for (int j = 0; j < 28; j++) {
        int ID = LVL_data[i][j];

        if (ID >= 1 && ID <= 3) {
          addObject(new Block(ID), px, py);
          Objects++;
        } else if (ID == 4) {
          addObject(new Box(), px, py);
          Objects++;
        } else if (ID == 5) {
          addObject(Player, px, py);
          Objects++;
        } else if (ID == 6) {
          addObject(new Scroll(), px, py);
          Coins++;
          Objects++;
        } else if (ID == 7 || ID == 8) {
          addObject(new Enemy(ID), px, py);
          Objects++;
        }

        px += TILESIZE;
      }

      px = TILESIZE / 2;
      py += TILESIZE;
    }

    maploaded = true;
  }

  public void act() {
    if (numberOfObjects() == Objects - Coins && maploaded || retry) {
      Player gPlayer = (Player) Player;
      gPlayer.Freeze(true);
      addObject(dummy, 0, 0);
      Greenfoot.setSpeed(70);
    }

    if (numberOfObjects() == 1) {
      removeObject(dummy);
      Allobjectsdestroyed = true;
      Player gPlayer = (Player) Player;
      gPlayer.Freeze(false);
      Greenfoot.setSpeed(50);
    }

    if (Allobjectsdestroyed) {
      Allobjectsdestroyed = false;

      if (!retry) Level++;

      retry = false;

      ChangeLevel(Level);

      GenerateLevel();
    }
  }

  public boolean isSolid(int x, int y) {
    x /= TILESIZE;
    y /= TILESIZE;

    if (x < 0 || x >= 28 || y < 0 || y >= 28) return true;

    switch (LVL_data[y][x]) {
      case 1:
      case 2:
        return true;

      default:
        return false;
    }
  }

  public boolean onSpike(int x, int y) {
    int px = x / TILESIZE;
    int py = y / TILESIZE;

    if (px < 0 || px >= 28 || py < 0 || py >= 28) return false;

    switch (LVL_data[py][px]) {
      case 3:
        if (y >= py * TILESIZE + 10) return true;
        else return false;

      default:
        return false;
    }
  }
