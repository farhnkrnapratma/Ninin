    /**
     * Generate all objects in the world based on lvlData:
     * - ID 1..3 → Block
     * - ID 4   → Box
     * - ID 5   → Player
     * - ID 6   → Scroll
     * - ID 7..8 → Enemy
     */
    public void generateLevel() {
        scrollCount = 0;
        objectCount = 0;

        int px = TILE_SIZE / 2;
        int py = TILE_SIZE / 2;

        for (int row = 0; row < 28; row++) {
            for (int col = 0; col < 28; col++) {
                int id = lvlData[row][col];

                if (id >= 1 && id <= 3) {
                    addObject(new Block(id), px, py);
                    objectCount++;
                } else if (id == 4) {
                    addObject(new Box(), px, py);
                    objectCount++;
                } else if (id == 5) {
                    addObject(player, px, py);
                    objectCount++;
                } else if (id == 6) {
                    addObject(new Scroll(), px, py);
                    scrollCount++;
                    objectCount++;
                } else if (id == 7 || id == 8) {
                    addObject(new Enemy(id), px, py);
                    objectCount++;
                }

                px += TILE_SIZE;
            }
            px = TILE_SIZE / 2;
            py += TILE_SIZE;
        }

        mapLoaded = true;
    }

    /**
     * act() is called each frame:
     * - If ESC pressed → pause
     * - If not paused, monitor object counts to trigger transitions:
     *   • If only Scrolls remain or retry flag set → freeze player, show dummy, speed up
     *   • If only dummy remains → remove dummy, mark allObjectsDestroyed, unfreeze player, reset speed
     *   • If allObjectsDestroyed → advance level or finish game
     */
    public void act() {
        // Pause on ESC
        if (Greenfoot.isKeyDown("escape")) {
            pauseGame();
            return;
        }

        if (!isPaused) {
            // Check if only Scrolls (or retry) remain → prepare transition
            if ((numberOfObjects() == objectCount - scrollCount && mapLoaded) || retry) {
                player.Freeze(false);
                addObject(dummy, 0, 0);
                Greenfoot.setSpeed(70);
            }

            // If only dummy remains → level cleared
            if (numberOfObjects() == 1) {
                removeObject(dummy);
                allObjectsDestroyed = true;
                player.Freeze(false);
                Greenfoot.setSpeed(50);
            }

            // Advance to next level or finish
            if (allObjectsDestroyed) {
                allObjectsDestroyed = false;
                if (!retry) {
                    level++;
                }
                retry = false;

                if (level > 5) {
                    Greenfoot.setWorld(new GameCompleted());
                    return;
                }

                changeLevel(level);
                generateLevel();
            }
        }
    }

    /**
     * Check if a world coordinate (x, y) is a solid tile (Block or BlockGrass).
     * Used by Collision logic.
     *
     * @param x World x-coordinate (pixels)
     * @param y World y-coordinate (pixels)
     * @return true if this position is solid or out-of-bounds, false otherwise
     */
    public boolean isSolid(int x, int y) {
        int col = x / TILE_SIZE;
        int row = y / TILE_SIZE;

        // Out-of-bounds treated as solid
        if (col < 0 || col >= 28 || row < 0 || row >= 28) {
            return true;
        }

        switch (lvlData[row][col]) {
            case 1:
            case 2:
                return true;
            default:
                return false;
        }
    }

    /**
     * Check if a coordinate (x, y) is on a spike tile and the player is on its lower half.
     *
     * @param x World x-coordinate (pixels)
     * @param y World y-coordinate (pixels)
     * @return true if this position is considered a “spike hit”, false otherwise
     */
    public boolean onSpike(int x, int y) {
        int col = x / TILE_SIZE;
        int row = y / TILE_SIZE;

        if (col < 0 || col >= 28 || row < 0 || row >= 28) {
            return false;
        }

        // Spike ID is 3; only lethal if y is below the tile’s midpoint
        if (lvlData[row][col] == 3) {
            return y >= row * TILE_SIZE + 10;
        }
        return false;
    }
